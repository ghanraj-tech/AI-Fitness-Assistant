def get_diet_plan(goal):

    if goal == "Weight Loss":

        return [

            "Oats",
            "Eggs",
            "Chicken Breast",
            "Rice",
            "Salad",
            "Fruits",
            "Milk"
        ]

    elif goal == "Muscle Gain":

        return [

            "Bananas",
            "Peanut Butter",
            "Eggs",
            "Chicken",
            "Paneer",
            "Rice",
            "Dry Fruits"
        ]

    else:

        return [

            "Balanced Diet",
            "Vegetables",
            "Protein",
            "Fruits"
        ]